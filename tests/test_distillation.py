"""Validate qudit_qec.distillation against KNOWN small codes from the literature.

Ground-truth targets (all small / low-compute, n <= 23, single process):

* Triorthogonal qutrit family ``[[9m-k, k, 2]]_3`` (arXiv:2403.06228):
    - ``[[20,7,2]]_3`` (m=3, k=7)  -> yield gamma = 1.51
    - ``[[14,4,2]]_3`` (m=2, k=4)  -> yield gamma = 1.81
  Built from the triorthogonal space ``T_m`` (Eq. of the paper), punctured.
* 11-qutrit Golay ``[[11,1,5]]_3`` (arXiv:2008.golay / used in 2408.00436):
    - simple weight enumerator A(z) = 1+528 z^6+7920 z^8+11000 z^9+23760 z^10+15840 z^11
    - MacWilliams B(z) matches the paper; conditions (1)&(2) hold => cubic suppression
    - threshold eps_* ~ 0.387
* Qubit sanity: the ``[[15,1,3]]_2`` Reed-Muller code -> yield gamma = log_2(15) ~ 2.46
  (the original Bravyi-Kitaev / Reed-Muller distillation code).
"""

from __future__ import annotations

import math

import galois
import numpy as np
import pytest

from qudit_qec import distillation as dist


GF3 = galois.GF(3)


# --------------------------------------------------------------------------- #
# builders for the known codes
# --------------------------------------------------------------------------- #
def _build_Tm(m: int) -> np.ndarray:
    """Triorthogonal space generator T_m (size 9m, dim 3m) per arXiv:2403.06228."""
    n = 9 * m
    w = np.array([i % 3 for i in range(n)])  # (0,1,2,0,1,2,...)
    rows = [w]
    for a in range(1, 3 * m):
        v = np.zeros(n, dtype=int)
        for i in range(1, n + 1):  # 1-indexed per paper
            if 3 * (a - 1) < i <= 3 * a:
                v[i - 1] = 1
            elif n - 3 < i <= n:
                v[i - 1] = 2
        rows.append(v)
    return np.array(rows) % 3


def _triortho_matrix(m: int, k: int) -> np.ndarray:
    """Puncture T_m at coords 3j+1 (j<k) -> triorthogonal matrix H of the code."""
    T = _build_Tm(m)
    cols_del = {3 * j for j in range(k)}  # 0-indexed 3j  (== 1-indexed 3j+1)
    keep = [c for c in range(9 * m) if c not in cols_del]
    return T[:, keep] % 3


def _triortho_css(m: int, k: int):
    """Build the qldpc CSSCode for the [[9m-k,k,2]]_3 triorthogonal code."""
    from qldpc.codes import CSSCode

    H = _triortho_matrix(m, k)
    Hf = GF3(H)
    # H_0 = self-orthogonal rows (zero cube); they generate S_x.
    cubes = np.array([int(np.sum((H[r] * H[r] * H[r]) % 3)) % 3 for r in range(H.shape[0])])
    H0 = H[cubes == 0]
    # S_z is generated by H^perp (right null space of H).
    Hperp = np.asarray(GF3(H).null_space()) % 3
    code = CSSCode(np.asarray(H0, dtype=int), Hperp.astype(int), field=3)
    return code, H


def _golay_css():
    """11-qutrit Golay [[11,1,5]]_3 CSS code from the self-dual extended Golay."""
    from qldpc.codes import CSSCode

    B = GF3([
        [0, 1, 1, 1, 1, 1],
        [1, 0, 1, 2, 2, 1],
        [1, 1, 0, 1, 2, 2],
        [1, 2, 1, 0, 1, 2],
        [1, 2, 2, 1, 0, 1],
        [1, 1, 2, 2, 1, 0],
    ])
    G = GF3(np.hstack([np.eye(6, dtype=int), np.asarray(B, dtype=int)]))  # [12,6,6] self-dual
    Gp = GF3(np.asarray(G)[:, :-1])           # punctured -> [11,6,5] ternary Golay C1
    C2 = Gp.null_space()                       # C1^perp = [11,5,6], self-orthogonal
    C2 = np.asarray(C2, dtype=int)
    code = CSSCode(C2, C2, field=3)            # Hx = Hz = C2  ->  [[11,1,5]]_3
    return code


def _reed_muller_15():
    """[[15,1,3]]_2 quantum Reed-Muller code: punctured RM(1,4), classic T distiller."""
    from qldpc.codes import CSSCode

    GF2 = galois.GF(2)
    # RM(1,4) generator rows over the 16 points of F_2^4, then puncture point 0000.
    pts = np.array([[(i >> b) & 1 for b in range(4)] for i in range(16)])
    rows = [np.ones(16, dtype=int)] + [pts[:, b] for b in range(4)]
    RM1 = np.array(rows) % 2                       # RM(1,4): [16,5]
    RM1p = RM1[:, 1:]                              # puncture coord 0 -> [15,5]
    # CSS [[15,1,3]]: Hx = RM1p (5 rows, self-orthogonal over F2 after puncture? use RM(1,4) shortened)
    # Standard construction: Hz = RM1p (the [15,5] punctured RM1),
    #                        Hx = its dual-contained [15, 10] = (RM1p)^perp restricted.
    # Use the textbook [[15,1,3]]: Hx = 4x15 (the degree-1 monomials), Hz = 10x15.
    # Build from punctured RM(1,4) and RM(2,4):
    def rm(r, mm):
        P = np.array([[(i >> b) & 1 for b in range(mm)] for i in range(2**mm)])
        monos = [np.ones(2**mm, dtype=int)]
        # degree-1
        for b in range(mm):
            monos.append(P[:, b])
        # degree-2
        if r >= 2:
            for a in range(mm):
                for b in range(a + 1, mm):
                    monos.append((P[:, a] * P[:, b]) % 2)
        return GF2(np.array(monos) % 2)

    RM1_4 = rm(1, 4)        # [16,5]
    RM2_4 = rm(2, 4)        # [16,11]
    # puncture coordinate 0
    H_RM1 = np.asarray(RM1_4)[:, 1:] % 2     # [15,5]
    H_RM2 = np.asarray(RM2_4)[:, 1:] % 2     # [15,11]
    # CSS: Sx = punctured RM(1,4) [15,5] minus all-ones? -> classic [[15,1,3]]:
    # Hx = 4 weight-8 rows (punctured degree-1), Hz = 10 rows (punctured RM2 \ RM1).
    # Simplest valid build: Hx = H_RM1 (rank-5 -> but need Hx Hz^T=0). Use:
    Hx = H_RM1                                # [15,5], rank 5? rows incl all-ones punctured
    # Hz = punctured RM(2,4) restricted to be dual to Hx with k=1:
    Hz = H_RM2
    code = CSSCode(Hx.astype(int), Hz.astype(int), field=2)
    return code


# --------------------------------------------------------------------------- #
# Triorthogonality
# --------------------------------------------------------------------------- #
def test_triortho_matrix_2014():
    """[[20,7,2]]_3 (m=3,k=7) is a triorthogonal *matrix* with 7 magic rows."""
    H = _triortho_matrix(3, 7)
    rep = dist.is_triorthogonal(None, p=3, generators=H)
    assert rep.is_matrix is True
    assert rep.n_magic_rows == 7          # k = 7 magic logicals
    assert rep.h0_self_orthogonal is True
    assert rep.sound is True
    assert bool(rep) is True


def test_triortho_matrix_1404():
    """[[14,4,2]]_3 (m=2,k=4) is a triorthogonal matrix with 4 magic rows."""
    H = _triortho_matrix(2, 4)
    rep = dist.is_triorthogonal(None, p=3, generators=H)
    assert rep.is_matrix is True
    assert rep.n_magic_rows == 4
    assert rep.h0_self_orthogonal is True


def test_unpunctured_Tm_is_strict_space():
    """The unpunctured T_2 space is a strict triorthogonal SPACE (all triples zero)."""
    T = _build_Tm(2)
    rep = dist.is_triorthogonal(None, p=3, generators=T)
    assert rep.is_space is True
    assert rep.n_magic_rows == 0
    assert rep.is_matrix is True            # space => matrix vacuously


def test_non_triorthogonal_random():
    """A generic random matrix is (almost surely) not triorthogonal."""
    rng = np.random.default_rng(0)
    M = rng.integers(0, 3, size=(4, 14))
    rep = dist.is_triorthogonal(None, p=3, generators=M)
    assert rep.is_matrix is False
    assert bool(rep) is False


def test_non_prime_flagged_not_sound():
    """GF(4) (prime power) must be flagged sound=False with a universality caveat."""
    M = np.array([[0, 1, 2, 3], [1, 1, 0, 0]])
    rep = dist.is_triorthogonal(None, p=4, generators=M)
    assert rep.sound is False
    assert any("not certify" in c or "structural" in c for c in rep.caveats)


# --------------------------------------------------------------------------- #
# Transversal gate level
# --------------------------------------------------------------------------- #
def test_transversal_gate_level_triortho():
    """Triorthogonal matrix => transversal 3rd-level (non-Clifford) T gate."""
    H = _triortho_matrix(3, 7)
    tg = dist.transversal_gate_level(None, p=3, generators=H)
    assert tg.level == 3
    assert "T" in tg.gate
    assert "9" in (tg.diagonal_phase or "")    # exp(2 pi i k / 9) for p=3


# --------------------------------------------------------------------------- #
# Weight enumerator (validated vs the paper's exact A(z), B(z))
# --------------------------------------------------------------------------- #
def test_golay_weight_enumerator_matches_paper():
    """Golay A(z) and MacWilliams B(z) reproduce arXiv:2408.00436 exactly."""
    code = _golay_css()
    we = dist.weight_enumerator(code, p=3)
    assert we.n == 11 and we.k == 1
    # paper A(z) = 1+528 z^6+7920 z^8+11000 z^9+23760 z^10+15840 z^11
    A_expected = {0: 1, 6: 528, 8: 7920, 9: 11000, 10: 23760, 11: 15840}
    A_got = {i: c for i, c in enumerate(we.A) if c}
    assert A_got == A_expected
    assert sum(we.A) == 3 ** (we.n - we.k)         # |S| = 3^10
    # paper B(z) = 1+528 z^5+528 z^6+15840 z^7+40920 z^8+129800 z^9+198000 z^10+145824 z^11
    B_expected = {0: 1, 5: 528, 6: 528, 7: 15840, 8: 40920, 9: 129800, 10: 198000, 11: 145824}
    B_got = {i: c for i, c in enumerate(we.B) if c}
    assert B_got == B_expected


