def test_yield_2014_triortho():
    """[[20,7,2]]_3 triorthogonal yield gamma = log_2(20/7) ~ 1.51 (paper)."""
    H = _triortho_matrix(3, 7)
    # use generators directly so k = #magic rows = 7, n = 20, d = 2
    my = dist.magic_state_yield(None, p=3, mode="triorthogonal", d=2, generators=H)
    assert my.distills is True
    assert my.n == 20 and my.k == 7 and my.d == 2
    assert my.noise_suppression_exponent == 2
    assert my.gamma == pytest.approx(1.51, abs=0.01)
    assert my.sound is True


def test_yield_1404_triortho():
    """[[14,4,2]]_3 triorthogonal yield gamma = log_2(14/4) ~ 1.81 (paper)."""
    H = _triortho_matrix(2, 4)
    my = dist.magic_state_yield(None, p=3, mode="triorthogonal", d=2, generators=H)
    assert my.gamma == pytest.approx(1.81, abs=0.01)
    assert my.n == 14 and my.k == 4


def test_yield_qubit_reed_muller_sanity():
    """Qubit [[15,1,3]]_2 Reed-Muller: yield gamma = log_3(15) ~ 2.46 (Bravyi-Kitaev)."""
    # Compute the yield directly from the known [[n,k,d]] = [[15,1,3]] parameters.
    n, k, d = 15, 1, 3
    gamma = math.log(n / k) / math.log(d)
    assert gamma == pytest.approx(2.46, abs=0.01)


def test_golay_strange_distills_cubic():
    """Golay [[11,1,5]]_3 distills the strange state with cubic suppression (2408.00436)."""
    code = _golay_css()
    my = dist.magic_state_yield(code, p=3, mode="strange")
    assert my.mode == "strange"
    assert my.distills is True
    assert my.noise_suppression_exponent == 3      # cubic
    # threshold eps_* ~ 0.387 per the paper (cheap cubic-root solve)
    assert my.threshold == pytest.approx(0.387, abs=0.02)
    assert my.sound is True


def test_strange_mode_rejects_non_qutrit():
    """strange-state yield must refuse non-qutrit p (it is GF(3)-specific)."""
    GF5 = galois.GF(5)
    # trivial tiny p=5 CSS placeholder via generators path is not enough; call with p=5
    with pytest.raises(NotImplementedError):
        # a dummy code-like object with field order 5
        class _Dummy:
            class field:
                order = 5

            matrix_x = np.zeros((1, 5), dtype=int)
            matrix_z = np.zeros((1, 5), dtype=int)

            def get_logical_ops(self, *_):
                return np.zeros((0, 5), dtype=int)

        dist.magic_state_yield(_Dummy(), p=5, mode="strange")


